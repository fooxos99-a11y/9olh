    question,
    answer,
    points,
    normalized_question
  FROM (
    SELECT
      question,
      answer,
      points,
      regexp_replace(
        translate(trim(lower(question)), '؟!.,،:;"''`()[]{}ـ', ''),
        '\s+',
        ' ',
        'g'
      ) AS normalized_question
    FROM proposed_questions
  ) prepared
  ORDER BY normalized_question, points, question

FROM target_category
JOIN normalized_proposed ON TRUE
WHERE NOT EXISTS (
  SELECT 1
  FROM category_questions existing
  WHERE existing.category_id = target_category.id
    AND regexp_replace(
      translate(trim(lower(existing.question)), '؟!.,،:;"''`()[]{}ـ', ''),
